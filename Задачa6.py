# Получаем значение n от пользователя
n = int(input("Введите натуральное число n: "))

# Инициализируем переменную для хранения результата (факториала)
factorial_result = 1

# Проверяем, что n неотрицательное
if n < 0:
    print("Ошибка: Факториал определен только для неотрицательных чисел.")
elif n == 0:
    print("Факториал 0 равен 1.")
else:
    # Используем цикл for для вычисления факториала
    for i in range(1, n + 1):
        factorial_result *= i

    # Выводим результат
    print(f"Факториал {n} равен {factorial_result}.")
