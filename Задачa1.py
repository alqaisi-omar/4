A = int(input("Введите значение A: "))
B = int(input("Введите значение B (A должно быть меньше или равно B): "))

# Проверка условия A ≤ B
if A > B:
    print("Ошибка: A должно быть меньше или равно B.")
else:
    # Использование цикла for и range() для вывода чисел от A до B
    for num in range(A, B + 1):
        print(num, end=" ")
