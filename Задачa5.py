# Получаем значения A и B от пользователя
A = int(input("Введите значение A: "))
B = int(input("Введите значение B: "))

# Проверяем, что A ≤ B
if A > B:
    print("Ошибка: A должно быть меньше или равно B.")
else:
    # Выводим числа от A до B включительно в порядке возрастания
    print("Числа от A до B включительно в порядке возрастания:")
    while A <= B:
        print(A, end=" ")
        A += 1

# 5. Вычисляем сумму кубов натуральных чисел от 1 до n
n = int(input("\nВведите натуральное число n: "))
sum_cubes = sum(i**3 for i in range(1, n + 1))

print(f"Сумма кубов натуральных чисел от 1 до {n} равна: {sum_cubes}")
