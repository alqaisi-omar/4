# Получаем значения A и B от пользователя
A = int(input("Введите значение A: "))
B = int(input("Введите значение B: "))

# Проверяем, что A > B
if A <= B:
    print("Ошибка: A должно быть больше B.")
else:
    # Используем цикл while для вывода нечетных чисел в порядке убывания
    while A >= B:
        # Проверяем, является ли текущее число нечетным
        if A % 2 != 0:
            print(A, end=" ")
        A -= 1
